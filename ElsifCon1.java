package trainingday6CONDITIONS;

public class ElsifCon1 {
	public static void main(String[] args) {
		int a = 5;
		if (a==1) {
			System.out.println("1 is true");
		}
		else if (a==3) {
			System.out.println("3 is true");
		}
		else if (a==5) {
			System.out.println("5 is true");
		}
		else {
			System.out.println("all are false");
		}
	}

}
