package trainingday6CONDITIONS;

public class Elsif11char {
	public static void main(String[] args) {
		char let = 'A';
        if (let=='a') {
			System.out.println(let+"is apple");
		}
		else if (let == 'B') {
			System.out.println(let+"is banana");
		}
		else if (let == 'c') {
			System.out.println(let+"is cherry");
		}
		else {
			System.out.println("all are false");
		}
	}
	}

