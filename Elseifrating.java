package trainingday6CONDITIONS;

public class Elseifrating {
	public static void main(String[] args) {
		int a = 92;
		if (a>=71&&a<=80) {
			System.out.println(a+"Bease");
		}
		else if (a>=81&&a<=90) {
			System.out.println(a+"RRR");
		}
		else if(a>=91&&a<=100) {
			System.out.println(a+"KGF");
		}
		else {
			System.out.println("all are good");
		}
	}

}
